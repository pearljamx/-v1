"""
Flask 扩展初始化模块

使用标准的 Flask 扩展工厂模式：
- 所有扩展实例在此处创建，但不绑定到特定的 app
- init_app(app) 函数负责将扩展注册到给定的 Flask app 实例

本系统使用 JSON 文件存储实验结果，不依赖数据库。
"""

from flask_cors import CORS

# ---------------------------------------------------------------------------
# 扩展实例（未初始化，等待 init_app 调用）
# ---------------------------------------------------------------------------

cors = CORS()

# ---------------------------------------------------------------------------
# 初始化入口
# ---------------------------------------------------------------------------


def init_app(app):
    """
    将本模块中定义的所有扩展注册到指定的 Flask app 实例。

    在应用工厂（如 create_app）中调用此函数，传入已创建的 Flask app。
    若未来需要添加新扩展，只需在此函数中追加一行初始化调用即可。

    Parameters
    ----------
    app : flask.Flask
        已创建的 Flask 应用实例。
    """
    origins = app.config.get("CORS_ORIGINS", "*")
    cors.init_app(app, origins=origins)
